#ifndef DOG_H
#define DOG_H

/**
 * struct dog - represents a dog
 * @name: name of the dog (string)
 * @age: age of the dog (float)
 * @owner: owner of the dog (string)
 *
 * Description: A structure to hold basic info about a dog.
 */
struct dog
{
    char *name;
    float age;
    char *owner;
};

#endif /* DOG_H */
